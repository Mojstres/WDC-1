mood = input("How are you feeling? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Keep it up bro, everything will be okay")
elif mood == "excited":
    print("Lets goooo!")
elif mood == "relaxed":
    print("I feel ya, open a beer and watch some netflix")
else:
    print("I don't recognize this mood.")
